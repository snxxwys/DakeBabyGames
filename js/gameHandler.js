function pastelColors(str) {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  const hue = Math.abs(hash % 360);
  return [
    `hsl(${hue}, 70%, 55%)`,
    `hsl(${(hue + 15) % 360}, 65%, 50%)`
  ];
}


function loadFile(index) {
  const file = files[index];
  title.textContent = file.replace("cl", "");
  fetch(`https://cdn.jsdelivr.net/gh/bubbls/ugs-singlefile/${file}.html?t=${Date.now()}`)
    .then(res => res.text())
    .then(html => {
      currentHTML = html;

      const blob = new Blob([html], { type: "text/html" });
      const url = URL.createObjectURL(blob);
      iframe.src = url;

      iframe.style.transform = "scale(1)";
      iframe.onload = () => {
        // Try to auto-scale based on width
        const scale = iframe.parentElement.clientWidth / iframe.contentWindow.document.documentElement.scrollWidth;
        iframe.style.transform = `scale(${scale})`;
      };
    });
}

