const favoritesList = document.getElementById("favoritesList");

function renderFavorites() {
  favoritesList.innerHTML = ""; // clear previous

  const favFiles = JSON.parse(localStorage.getItem("favorites") || "[]");

  favFiles.forEach(file => {
    const card = document.createElement("div");
    card.className = "game-card";

    const [c1, c2] = pastelColors(file);
    card.style.setProperty("--color1", c1);
    card.style.setProperty("--color2", c2);

    const title = document.createElement("p");
    title.className = "game-title";
    title.textContent = file.replace(/^cl/i, "").replace(/[-_]/g, " ");

    // Remove button
    const removeBtn = document.createElement("button");
    removeBtn.className = "favorite-btn active"; // reuse styles
    removeBtn.innerHTML = "✕";
    removeBtn.onclick = e => {
      e.stopPropagation(); // prevent loading the game
      let currentFavorites = JSON.parse(localStorage.getItem("favorites") || "[]");
      currentFavorites = currentFavorites.filter(f => f !== file);
      localStorage.setItem("favorites", JSON.stringify(currentFavorites));

      // Update both favorites bar and main game cards
      renderFavorites();
      updateMainListFavoriteState(file, false);
    };

    card.appendChild(removeBtn);
    card.appendChild(title);
    favoritesList.appendChild(card);

    card.onclick = () => {
      loadFile(files.indexOf(file))
      iframe.scrollIntoView({ behavior: "smooth", block: "end" });
    };
  });
}

// Helper function to toggle favorite button state on main list
function updateMainListFavoriteState(file, isActive) {
  const allCards = document.querySelectorAll("#gameList .game-card");
  allCards.forEach(card => {
    const title = card.querySelector(".game-title").textContent.replace(/\s/g, "").toLowerCase();
    if (title.includes(file.replace(/^cl/i, "").replace(/[-_]/g, "").toLowerCase())) {
      const btn = card.querySelector(".favorite-btn");
      if (btn) btn.classList.toggle("active", isActive);
    }
  });
}

function addFavoriteByName(name) {
  // Get current favorites from localStorage
  const favorites = JSON.parse(localStorage.getItem("favorites") || "[]");

  // Avoid duplicates
  if (!favorites.includes(name)) {
    favorites.push(name);
    localStorage.setItem("favorites", JSON.stringify(favorites));
  }

  // Update the favorites bar
  renderFavorites();

  // Optionally, update the star button on the main list
  updateMainListFavoriteState(name, true);
}

function favoriteCurrent() {
  if (iframe.src) {
    addFavoriteByName(files[currentIndex]);
  }
}

